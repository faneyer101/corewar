#include "libft.h"

void	ft_print_bits(unsigned char bit)
{
	int	i;

	i = 128;
	while (i > 0)
	{
		if (i & bit)
			write(1, "1", 1);
		else
			write(1, "0", 1);
		i >>= 1;
	}
}
